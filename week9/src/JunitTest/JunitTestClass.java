package JunitTest;

public class JunitTestClass {
	// square method
	public int square(int n) {
		return n*n;
	}
	
	// sum function
	public int sum(int a, int b) {
		return a+b;
	}
	
	// Subtraction function
	public int Substract(int a, int b) {
		if(a>b) return a-b;
		return b-a;
	}
		
	// Division function
	public int Division(int a, int b) {
		return (int)a/b;
	}
}
